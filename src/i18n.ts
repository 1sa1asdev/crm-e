export const translations: Record<Lang, Record<string, string>> = { en, sv }

/** Replace {placeholder} tokens in a translation string */
export function interpolate(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s
  return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`))
}
